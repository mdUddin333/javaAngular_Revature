
public class Example {

	public static void main(String[] args) {
		int i=200;
		// cast to a short
		short s=(short) i;
		System.out.println("cast to a short: "+s);
	
		//Casting an int to a double
		int a=200;
		// cast to a short
		short st=(short) a;
		System.out.println(st);
		// cast to a double
		
		double d=(double) a;
		System.out.println("cast to a double: "+d);
		
		// cast to a byte
		
		byte b=(byte) i;
		System.out.println("cast to a byte: "+b);
	}

}
