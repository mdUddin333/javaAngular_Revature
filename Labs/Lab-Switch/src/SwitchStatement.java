
public class SwitchStatement {

	public static void main(String[] args) {
		int x=5;
		
		
		switch (x) {
		
		case 8:
			System.out.println("case 8 ran");
			break;
		default:
			System.out.println("This is default case ran.");
			break;
		case 9:
			System.out.println("case 9 ran");
			break;
		case 10:
			System.out.println("case 10 ran");
			//break;

		
		}
	}

}
